package de.dm.mail2blog;

public class MailConfigurationManagerException extends Exception {

    public MailConfigurationManagerException() {}
    public MailConfigurationManagerException(String message) { super(message); }
    public MailConfigurationManagerException(Throwable cause) { super(cause); }
    public MailConfigurationManagerException(String message, Throwable cause) { super(message, cause); }
}
