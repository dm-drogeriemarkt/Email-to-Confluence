package de.dm.mail2blog;

public class FileTypeBucketException extends Exception {
    public FileTypeBucketException() {}
    public FileTypeBucketException(String message) { super(message); }
    public FileTypeBucketException(Throwable cause) { super(cause); }
    public FileTypeBucketException(String message, Throwable cause) { super(message, cause); }
}
